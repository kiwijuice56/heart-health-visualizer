static void sMultiWordMul(const unsigned int u1[], const unsigned int u2[],
  unsigned int y[])
{
  unsigned int cb;
  unsigned int cb1;
  int k;
  unsigned int yk;
  bool isNegative1;
  bool isNegative2;
  isNegative1 = ((u1[1] & 2147483648U) != 0U);
  isNegative2 = ((u2[1] & 2147483648U) != 0U);
  cb1 = 1U;

  // Initialize output to zero
  for (k = 0; k < 3; k++) {
    y[k] = 0U;
  }

  for (int i{0}; i < 2; i++) {
    unsigned int a0;
    unsigned int a1;
    unsigned int cb2;
    unsigned int u1i;
    cb = 0U;
    u1i = u1[i];
    if (isNegative1) {
      u1i = ~u1i + cb1;
      cb1 = (u1i < cb1);
    }

    a1 = u1i >> 16U;
    a0 = u1i & 65535U;
    cb2 = 1U;
    k = i;
    for (int j{0}; j < 2; j++) {
      unsigned int b1;
      unsigned int w01;
      unsigned int w10;
      u1i = u2[j];
      if (isNegative2) {
        u1i = ~u1i + cb2;
        cb2 = (u1i < cb2);
      }

      b1 = u1i >> 16U;
      u1i &= 65535U;
      w10 = a1 * u1i;
      w01 = a0 * b1;
      yk = y[k] + cb;
      cb = (yk < cb);
      u1i *= a0;
      yk += u1i;
      cb += (yk < u1i);
      u1i = w10 << 16U;
      yk += u1i;
      cb += (yk < u1i);
      u1i = w01 << 16U;
      yk += u1i;
      cb += (yk < u1i);
      y[k] = yk;
      cb += w10 >> 16U;
      cb += w01 >> 16U;
      cb += a1 * b1;
      k++;
    }

    if (k < 3) {
      y[k] = cb;
    }
  }

  // Apply sign
  if (isNegative1 != isNegative2) {
    cb = 1U;
    for (k = 0; k < 3; k++) {
      yk = ~y[k] + cb;
      y[k] = yk;
      cb = (yk < cb);
    }
  }
}

