static void MultiWordAdd(const unsigned int u1[], const unsigned int u2[],
  unsigned int y[])
{
  unsigned int carry{ 0U };

  for (int i{0}; i < 2; i++) {
    unsigned int u1i;
    unsigned int yi;
    u1i = u1[i];
    yi = (u1i + u2[i]) + carry;
    y[i] = yi;
    if (carry != 0U) {
      carry = (yi <= u1i);
    } else {
      carry = (yi < u1i);
    }
  }
}

static void MultiWordNeg(const unsigned int u1[], unsigned int y[])
{
  unsigned int carry{ 1U };

  for (int i{0}; i < 2; i++) {
    unsigned int yi;
    yi = ~u1[i] + carry;
    y[i] = yi;
    carry = (yi < carry);
  }
}

