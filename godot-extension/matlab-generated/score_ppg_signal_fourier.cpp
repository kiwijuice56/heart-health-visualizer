static int uMultiWordDiv(unsigned int a[], unsigned int b[], unsigned int q[],
  unsigned int r[])
{
  int nzb;
  int tpi;
  int y;
  nzb = 2;
  tpi = 1;
  while ((nzb > 0) && (b[tpi] == 0U)) {
    nzb--;
    tpi--;
  }

  if (nzb > 0) {
    int kq;
    int nza;
    nza = 2;
    for (kq = 0; kq < 2; kq++) {
      q[kq] = 0U;
    }

    tpi = 1;
    while ((nza > 0) && (a[tpi] == 0U)) {
      nza--;
      tpi--;
    }

    if ((nza > 0) && (nza >= nzb)) {
      int na1;
      int nb1;
      nb1 = nzb - 1;
      na1 = nza - 1;
      for (kq = 0; kq < 2; kq++) {
        r[kq] = 0U;
      }

      // Quick return if dividend and divisor fit into single word.
      if (nza == 1) {
        unsigned int ak;
        unsigned int bk;
        unsigned int qk;
        ak = a[0];
        bk = b[0];
        qk = ak / bk;
        q[0] = qk;
        r[0] = ak - qk * bk;
        y = 7;
      } else {
        unsigned int kba;
        unsigned int kbb;
        unsigned int t;

        // Remove leading zeros from both, dividend and divisor.
        kbb = 1U;
        t = b[nzb - 1] >> 1U;
        while (t != 0U) {
          kbb++;
          t >>= 1U;
        }

        kba = 1U;
        t = a[1] >> 1U;
        while (t != 0U) {
          kba++;
          t >>= 1U;
        }

        // Quick return if quotient is zero.
        if ((nzb < 2) || (kba >= kbb)) {
          unsigned int ak;
          unsigned int bk;
          int ka;
          unsigned int mask;
          unsigned int nba;
          unsigned int nbb;
          unsigned int qk;
          unsigned int tnb;
          nba = kba + 32U;
          nbb = static_cast<unsigned int>(nzb - 1) * 32U + kbb;

          // Normalize b.
          if (kbb != 32U) {
            bk = b[nzb - 1];
            kq = nzb - 1;
            while (kq > 0) {
              t = bk << (32U - kbb);
              bk = b[0];
              t |= bk >> kbb;
              b[1] = t;
              kq = 0;
            }

            b[0] = bk << (32U - kbb);
            mask = ~((1U << (32U - kbb)) - 1U);
          } else {
            mask = MAX_uint32_T;
          }

          // Initialize quotient to zero.
          tnb = 0U;
          y = 0;

          // Until exit conditions have been met, do
          do {
            // Normalize a
            if (kba != 32U) {
              tnb = (tnb - kba) + 32U;
              ak = a[na1];
              ka = na1;
              while (ka > 0) {
                t = ak << (32U - kba);
                ak = a[0];
                t |= ak >> kba;
                a[1] = t;
                ka = 0;
              }

              a[0] = ak << (32U - kba);
            }

            // Compare b against the a.
            ak = a[na1];
            bk = b[nzb - 1];
            if (nzb - 1 == 0) {
              qk = mask;
            } else {
              qk = MAX_uint32_T;
            }

            if ((ak & qk) == bk) {
              tpi = 0;
              ka = na1;
              kq = nzb - 1;
              while ((tpi == 0) && (kq > 0)) {
                ka--;
                ak = a[ka];
                kq = 0;
                bk = b[0];
                if ((ak & mask) != bk) {
                  if (ak > bk) {
                    tpi = 1;
                  } else {
                    tpi = -1;
                  }
                }
              }
            } else if (ak > bk) {
              tpi = 1;
            } else {
              tpi = -1;
            }

            // If the remainder in a is still greater or equal to b, subtract normalized divisor from a.
            if ((tpi >= 0) || (nba > nbb)) {
              qk = nba - nbb;

              // If the remainder and the divisor are equal, set remainder to zero.
              if (tpi == 0) {
                ka = na1;
                kq = nzb - 1;
                while (kq > 0) {
                  a[ka] = 0U;
                  ka--;
                  kq = 0;
                }

                a[ka] -= b[0];
              } else {
                // Otherwise, subtract the divisor from the remainder
                if (tpi < 0) {
                  ak = a[na1];
                  kba = 31U;
                  ka = na1;
                  while (ka > 0) {
                    t = ak << 1U;
                    ak = a[0];
                    t |= ak >> 31U;
                    a[1] = t;
                    ka = 0;
                  }

                  a[0] = ak << 1U;
                  tnb++;
                  qk--;
                }

                bk = 0U;
                ka = (na1 - nzb) + 1;
                for (kq = 0; kq < nzb; kq++) {
                  t = a[ka];
                  ak = (t - b[kq]) - bk;
                  if (bk != 0U) {
                    bk = (ak >= t);
                  } else {
                    bk = (ak > t);
                  }

                  a[ka] = ak;
                  ka++;
                }
              }

              // Update the quotient.
              tpi = static_cast<int>(qk) / 32;
              q[tpi] |= 1U << (qk - static_cast<unsigned int>(tpi) * 32U);

              // Remove leading zeros from the remainder and check whether the exit conditions have been met.
              tpi = na1;
              while ((nza > 0) && (a[tpi] == 0U)) {
                nza--;
                tpi--;
              }

              if (nza >= nzb) {
                na1 = nza - 1;
                kba = 1U;
                t = a[nza - 1] >> 1U;
                while (t != 0U) {
                  kba++;
                  t >>= 1U;
                }

                nba = (static_cast<unsigned int>(nza - 1) * 32U + kba) - tnb;
                if (nba < nbb) {
                  y = 2;
                }
              } else if (nza == 0) {
                y = 1;
              } else {
                na1 = 0;
                y = 4;
              }
            } else {
              y = 3;
            }
          } while (y == 0);

          // Return the remainder.
          if (y == 1) {
            r[0] = a[0];
          } else {
            tpi = static_cast<int>(tnb) / 32;
            qk = tnb - static_cast<unsigned int>(tpi) * 32U;
            if (qk == 0U) {
              ka = tpi;
              for (kq = 0; kq <= nb1; kq++) {
                r[kq] = a[ka];
                ka++;
              }
            } else {
              ak = a[tpi];
              kq = 0;
              for (ka = tpi + 1; ka <= na1; ka++) {
                t = ak >> qk;
                ak = a[ka];
                t |= ak << (32U - qk);
                r[kq] = t;
                kq++;
              }

              r[kq] = ak >> qk;
            }
          }

          // Restore b.
          if (kbb != 32U) {
            bk = b[0];
            for (kq = 0; kq < nb1; kq++) {
              t = bk >> (32U - kbb);
              bk = b[1];
              t |= bk << kbb;
              b[0] = t;
            }

            b[kq] = bk >> (32U - kbb);
          }
        } else {
          for (kq = 0; kq < 2; kq++) {
            r[kq] = a[kq];
          }

          y = 6;
        }
      }
    } else {
      for (kq = 0; kq < 2; kq++) {
        r[kq] = a[kq];
      }

      y = 5;
    }
  } else {
    y = -1;
  }

  return y;
}

