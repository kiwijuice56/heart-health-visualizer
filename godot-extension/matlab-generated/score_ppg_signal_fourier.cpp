void average_pulse(const coder::array<double, 2U> &processed_ppg_signal, double
                   b_average_pulse[500])
{
  coder::array<double, 2U> b_processed_ppg_signal;
  coder::array<double, 2U> indices;
  coder::array<double, 2U> y;
  int i;
  int num_pulses;

  // AVERAGE_PULSE
  //  Returns the average pulse waveform of the processed PPG signal
  split_ppg_signal(processed_ppg_signal, indices);
  std::memset(&b_average_pulse[0], 0, 500U * sizeof(double));
  num_pulses = 0;
  i = indices.size(1);
  for (int b_i{0}; b_i <= i - 2; b_i++) {
    double b_dv[500];
    double ppg_pulse[500];
    double b_y;
    double d;
    int b_loop_ub;
    int i2;
    int loop_ub;
    b_y = indices[b_i];
    d = indices[b_i + 1];
    if (b_y > d) {
      i2 = 0;
      loop_ub = 0;
    } else {
      i2 = static_cast<int>(b_y) - 1;
      loop_ub = static_cast<int>(d);
    }

    // PREPROCESS_PPG_PULSE
    //  Returns a processed copy of a PPG pulse.
    //  Normalize and rescale to [0, 1] range
    b_loop_ub = loop_ub - i2;
    b_processed_ppg_signal.set_size(1, b_loop_ub);
    for (int i1{0}; i1 < b_loop_ub; i1++) {
      b_processed_ppg_signal[i1] = processed_ppg_signal[i2 + i1];
    }

    b_y = coder::combineVectorElements(b_processed_ppg_signal) / static_cast<
      double>(b_loop_ub);
    b_processed_ppg_signal.set_size(1, b_loop_ub);
    for (int i1{0}; i1 < b_loop_ub; i1++) {
      b_processed_ppg_signal[i1] = processed_ppg_signal[i2 + i1] - b_y;
    }

    coder::b_abs(b_processed_ppg_signal, y);
    b_y = coder::internal::maximum(y);
    b_processed_ppg_signal.set_size(1, b_processed_ppg_signal.size(1));
    loop_ub = b_processed_ppg_signal.size(1) - 1;
    for (int i1{0}; i1 <= loop_ub; i1++) {
      b_processed_ppg_signal[i1] = b_processed_ppg_signal[i1] / b_y;
    }

    //  Interpolate using cubic interpolation
    //  We want a fixed amount of samples for all pulses (500)
    if (b_processed_ppg_signal.size(1) < 1) {
      y.set_size(1, 0);
    } else {
      y.set_size(1, b_loop_ub);
      for (int i1{0}; i1 <= loop_ub; i1++) {
        y[i1] = static_cast<double>(i1) + 1.0;
      }
    }

    coder::linspace(static_cast<double>(b_processed_ppg_signal.size(1)), b_dv);
    coder::interp1(y, b_processed_ppg_signal, b_dv, ppg_pulse);
    for (int i1{0}; i1 < 500; i1++) {
      b_average_pulse[i1] += ppg_pulse[i1];
    }

    num_pulses++;
  }

  for (int i1{0}; i1 < 500; i1++) {
    b_average_pulse[i1] /= static_cast<double>(num_pulses);
  }
}

void preprocess_ppg_signal(const coder::array<double, 2U> &ppg_signal, const
  coder::array<int64m_T, 2U> &timestamps, coder::array<double, 2U>
  &processed_ppg_signal)
{
  static const int64m_T r3{ { 150U, 0U }// chunks
  };

  coder::array<double, 2U> b_processed_ppg_signal;
  coder::array<double, 2U> b_timestamps;
  coder::array<double, 2U> r;
  int96m_T r4;
  int96m_T r6;
  int96m_T r7;
  int96m_T r8;
  double y;
  int loop_ub;

  // PREPROCESS_PPG_SIGNAL
  //  Returns a processed copy of a raw PPG signal (red
  //  channel values of a camera recording at the given timestamps,
  //  in milliseconds). Assumes raw PPG signal has the correct orientation.
  //  Normalize and rescale to [0, 1] range
  y = coder::combineVectorElements(ppg_signal) / static_cast<double>
    (ppg_signal.size(1));
  loop_ub = ppg_signal.size(1);
  processed_ppg_signal.set_size(1, ppg_signal.size(1));
  for (int i{0}; i < loop_ub; i++) {
    processed_ppg_signal[i] = ppg_signal[i] - y;
  }

  coder::b_abs(processed_ppg_signal, r);
  y = coder::internal::maximum(r);
  processed_ppg_signal.set_size(1, processed_ppg_signal.size(1));
  loop_ub = processed_ppg_signal.size(1) - 1;
  for (int i{0}; i <= loop_ub; i++) {
    processed_ppg_signal[i] = processed_ppg_signal[i] / y;
  }

  //  Interpolate using cubic interpolation
  //  Hz
  //  Milliseconds -> Seconds
  loop_ub = timestamps.size(1);
  b_timestamps.set_size(1, timestamps.size(1));
  for (int i{0}; i < loop_ub; i++) {
    b_timestamps[i] = sMultiWord2Double((const unsigned int *)&timestamps[i].
      chunks[0U]);
  }

  int64m_T r1;
  int64m_T r2;
  int64m_T r5;
  r1 = timestamps[timestamps.size(1) - 1];
  r2 = r3;
  sMultiWord2MultiWord((const unsigned int *)&r1.chunks[0U], (unsigned int *)
                       &r4.chunks[0U], 3);
  r5 = timestamps[0];
  sMultiWord2MultiWord((const unsigned int *)&timestamps[0].chunks[0U],
                       (unsigned int *)&r6.chunks[0U], 3);
  MultiWordSub((const unsigned int *)&r4.chunks[0U], (const unsigned int *)
               &r6.chunks[0U], (unsigned int *)&r7.chunks[0U], 3);
  sMultiWord2sMultiWordSat((const unsigned int *)&r7.chunks[0U], (unsigned int *)
    &r5.chunks[0U]);
  r5 = coder::internal::i64ddiv(r5);
  sMultiWordMul((const unsigned int *)&r3.chunks[0U], (const unsigned int *)
                &r5.chunks[0U], (unsigned int *)&r8.chunks[0U]);
  sMultiWord2sMultiWordSat((const unsigned int *)&r8.chunks[0U], (unsigned int *)
    &r2.chunks[0U]);
  coder::linspace(sMultiWord2Double((const unsigned int *)&timestamps[0].chunks
    [0U]), sMultiWord2Double((const unsigned int *)&r1.chunks[0U]), r2, r);
  b_processed_ppg_signal.set_size(1, processed_ppg_signal.size(1));
  loop_ub = processed_ppg_signal.size(0) * processed_ppg_signal.size(1) - 1;
  for (int i{0}; i <= loop_ub; i++) {
    b_processed_ppg_signal[i] = processed_ppg_signal[i];
  }

  coder::interp1(b_timestamps, b_processed_ppg_signal, r, processed_ppg_signal);

  //  Detrend
  b_processed_ppg_signal.set_size(1, processed_ppg_signal.size(1));
  loop_ub = processed_ppg_signal.size(0) * processed_ppg_signal.size(1) - 1;
  for (int i{0}; i <= loop_ub; i++) {
    b_processed_ppg_signal[i] = processed_ppg_signal[i];
  }

  coder::detrend(b_processed_ppg_signal, processed_ppg_signal);
}

